export function getTrailBySlug(slug: string): Trail | undefined {
  return trails.find((t) => t.slug === slug);
}

export function getTrailsAsGeoJSON() {
  return {
    type: "FeatureCollection" as const,
    features: trails.map((trail) => ({
      type: "Feature" as const,
      properties: {
        slug: trail.slug,
        name: trail.name,
        difficulty: trail.difficulty,
        distance_km: trail.distance_km,
        elevation_gain_m: trail.elevation_gain_m,
        elevation_high_m: trail.elevation_high_m,
        estimated_hours: trail.estimated_hours,
        type: trail.type,
        passport_peak: trail.passport_peak || false,
        passport_tier: trail.passport_tier || null,
      },
      geometry: {
        type: "LineString" as const,
        coordinates: trail.coordinates,
      },
    })),
  };
}

export function getTrailheadsAsGeoJSON() {
  return {
    type: "FeatureCollection" as const,
    features: trails.map((trail) => ({
      type: "Feature" as const,
      properties: {
        slug: trail.slug,
        name: trail.name,
        difficulty: trail.difficulty,
        distance_km: trail.distance_km,
        elevation_gain_m: trail.elevation_gain_m,
        passport_peak: trail.passport_peak || false,
        passport_tier: trail.passport_tier || null,
      },
      geometry: {
        type: "Point" as const,
        coordinates: trail.trailhead,
      },
    })),
  };
}
